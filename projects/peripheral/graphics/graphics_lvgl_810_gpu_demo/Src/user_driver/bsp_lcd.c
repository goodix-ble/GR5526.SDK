void lcd_ca_ra_set(uint16_t x1, uint16_t x2, uint16_t y1, uint16_t y2)
{
    uint8_t data[4];

    //x1 += 4;
    //x2 += 4;

    //printf("x(%d.%d) y(%d.%d) \r\n", x1,x2,y1,y2);

    data[0] = ((x1 + 40) & 0xff00) >> 8;
    data[1] = (x1 + 40) & 0x00ff;
    data[2] = ((x2+ 40)& 0xff00) >> 8;
    data[3] = (x2+40) & 0x00ff;
    lcd_write(0x2a, data, 4);

    data[0] = ((y1+40) & 0xff00) >> 8;
    data[1] = (y1 + 40)& 0x00ff;
    data[2] = ((y2 + 40) & 0xff00) >> 8;
    data[3] = (y2 + 40) & 0x00ff;
    lcd_write(0x2b, data, 4);
}

